package com.github.augustocaixeta.objetos;

public class DadosSaida {
    private float x1L;
    private float x2L;
    private float delta;
    private float Vx;
    private float Vy;

    public float getX1L() {
        return x1L;
    }

    public void setX1L(float x1L) {
        this.x1L = x1L;
    }

    public float getX2L() {
        return x2L;
    }

    public void setX2L(float x2L) {
        this.x2L = x2L;
    }

    public float getDelta() {
        return delta;
    }

    public void setDelta(float delta) {
        this.delta = delta;
    }

    public float getVx() {
        return Vx;
    }

    public void setVx(float Vx) {
        this.Vx = Vx;
    }

    public float getVy() {
        return Vy;
    }

    public void setVy(float Vy) {
        this.Vy = Vy;
    }
}
